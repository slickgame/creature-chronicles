import { existsSync, mkdirSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join, relative, resolve, sep } from "node:path";

const projectRoot = resolve(process.cwd());
const scenesRoot = join(projectRoot, "public", "images", "breeding", "scenes");
const outcomesRoot = join(scenesRoot, "outcomes");
const outputPath = join(projectRoot, "src", "data", "generatedBreedingSceneImages.ts");
const supportedExtensions = new Set([".png", ".jpg", ".jpeg", ".webp", ".gif"]);

function naturalCompare(left, right) {
  return left.localeCompare(right, undefined, { numeric: true, sensitivity: "base" });
}

function extensionOf(filename) {
  const dotIndex = filename.lastIndexOf(".");
  return dotIndex >= 0 ? filename.slice(dotIndex).toLowerCase() : "";
}

function toPublicUrl(absolutePath) {
  const publicRoot = join(projectRoot, "public");
  const relativePath = relative(publicRoot, absolutePath);
  return `/${relativePath.split(sep).map(encodeURIComponent).join("/")}`;
}

function collectImages(folderPath) {
  return readdirSync(folderPath, { withFileTypes: true })
    .filter((entry) => entry.isFile() && supportedExtensions.has(extensionOf(entry.name)))
    .map((entry) => entry.name)
    .sort(naturalCompare)
    .map((filename) => toPublicUrl(join(folderPath, filename)));
}

function normalizeOutcomeFolder(folderName) {
  const normalized = folderName
    .trim()
    .toLowerCase()
    .replace(/[_-]+/g, " ")
    .replace(/\s+/g, " ");

  if (["pregnancy", "pregnant", "success", "successful"].includes(normalized)) return "pregnancy";
  if (["failed", "failure", "not pregnant", "no pregnancy", "unsuccessful"].includes(normalized)) return "failed";
  if (["blocked", "already pregnant", "pregnancy blocked"].includes(normalized)) return "blocked";
  return null;
}

const receiverImages = {};
const receiverGiverImages = {};
const receiverOutcomeImages = {};

if (existsSync(scenesRoot)) {
  const folders = readdirSync(scenesRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && entry.name.toLowerCase().endsWith(" receiver"))
    .sort((a, b) => naturalCompare(a.name, b.name));

  for (const folder of folders) {
    const receiverFamily = folder.name.slice(0, -" receiver".length).trim().toLowerCase();
    const folderPath = join(scenesRoot, folder.name);
    const images = collectImages(folderPath);
    if (images.length > 0) receiverImages[receiverFamily] = images;

    const giverFolders = readdirSync(folderPath, { withFileTypes: true })
      .filter((entry) => entry.isDirectory())
      .sort((a, b) => naturalCompare(a.name, b.name));

    for (const giverFolder of giverFolders) {
      const giverFamily = giverFolder.name.trim().toLowerCase();
      const giverImages = collectImages(join(folderPath, giverFolder.name));
      if (giverImages.length > 0) receiverGiverImages[`${giverFamily}_to_${receiverFamily}`] = giverImages;
    }
  }
}

if (existsSync(outcomesRoot)) {
  const receiverFolders = readdirSync(outcomesRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .sort((a, b) => naturalCompare(a.name, b.name));

  for (const receiverFolder of receiverFolders) {
    const receiverFamily = receiverFolder.name.trim().toLowerCase();
    const receiverPath = join(outcomesRoot, receiverFolder.name);
    const outcomeFolders = readdirSync(receiverPath, { withFileTypes: true })
      .filter((entry) => entry.isDirectory())
      .sort((a, b) => naturalCompare(a.name, b.name));

    for (const outcomeFolder of outcomeFolders) {
      const outcome = normalizeOutcomeFolder(outcomeFolder.name);
      if (!outcome) continue;

      const images = collectImages(join(receiverPath, outcomeFolder.name));
      if (images.length > 0) receiverOutcomeImages[`${receiverFamily}_${outcome}`] = images;
    }
  }
}

const generatedSource = `// AUTO-GENERATED by scripts/generate-breeding-scene-manifest.mjs.\n// Pairing: root files in <receiver> receiver/ apply to any giver.\n// Pairing: files in <receiver> receiver/<giver>/ override the root pool for that exact pairing.\n// Outcomes: files in outcomes/<receiver>/<pregnant|not pregnant|blocked>/ are selected by receiver family and result.\n\nexport const GENERATED_RECEIVER_PAIRING_PATHS: Readonly<Record<string, readonly string[]>> = ${JSON.stringify(receiverImages, null, 2)} as const;\n\nexport const GENERATED_GIVER_TO_RECEIVER_PAIRING_PATHS: Readonly<Record<string, readonly string[]>> = ${JSON.stringify(receiverGiverImages, null, 2)} as const;\n\nexport const GENERATED_RECEIVER_OUTCOME_PATHS: Readonly<Record<string, readonly string[]>> = ${JSON.stringify(receiverOutcomeImages, null, 2)} as const;\n`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, generatedSource, "utf8");

const receiverSummary = Object.entries(receiverImages).map(([family, images]) => `${family}: ${images.length}`);
const pairSummary = Object.entries(receiverGiverImages).map(([pair, images]) => `${pair}: ${images.length}`);
const outcomeSummary = Object.entries(receiverOutcomeImages).map(([key, images]) => `${key}: ${images.length}`);
const summary = [...receiverSummary, ...pairSummary, ...outcomeSummary].join(", ");
console.log(`Generated breeding scene manifest${summary ? ` (${summary})` : " (no scene folders found)"}.`);
